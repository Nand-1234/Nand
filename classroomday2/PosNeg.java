package classroomday2;

public class PosNeg {

	public static void main(String[] args) {
		int num = -40;

		
		if (num<0) {
			System.out.println("The number is negative");
			num=(-1)*num;
			System.out.println("The converted positive number is:" + num);
			
		}
		else 
		{
		    System.out.println("The number is positive");
		}
	}

}
